alumnos = ["Juan", "María", "Pedro", "Ana"]

# Agregar un elemento al final de la lista
alumnos.append("Luis")

# Remover un elemento de la lista
alumnos.remove("María")
alumnos.pop(0)

# Extender una lista con otra
alumnos.extend(["Carlos", "Luisa"])

# Ordenar una lista
alumnos.sort(reverse=True)

print(alumnos)